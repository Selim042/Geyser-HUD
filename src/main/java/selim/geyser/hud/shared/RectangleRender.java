package selim.geyser.hud.shared;

public class RectangleRender {

	public final int x;
	public final int y;
	public final int width;
	public final int height;
	public final int color;

	public RectangleRender(int x, int y, int width, int height, int color) {
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
		this.color = color;
	}

}
